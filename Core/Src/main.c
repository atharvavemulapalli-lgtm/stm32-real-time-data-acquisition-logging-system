void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART2)
	{
		if(rxData == 'l')
				systemMode = LIGHT_MODE;
			else if(rxData == 't')
				systemMode = TEMP_MODE;
			else{
				sprintf(msg, "Only t or l\r\n");
				HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
			}

		HAL_UART_Receive_IT(&huart2, (uint8_t*)&rxData, 1);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM2)
	{
		dhtCounter++;

		//Read LDR Value
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
		adcValue = HAL_ADC_GetValue(&hadc1);
		HAL_ADC_Stop(&hadc1);

		//Convert ADC Reading to PWM duty cycle
		duty = 9999 - ((4095 - adcValue)*9999) /4095;

		//Update RGB LED based on selected mode
		if(systemMode == LIGHT_MODE){
			switch(mode)
			{
			case WHITE:
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, duty);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, duty);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, duty);
		  		    break;

			case RED:
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, duty);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 0);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 0);
		  		    break;

			case GREEN:
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 0);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, duty);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 0);
		  		    break;

			case BLUE:
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 0);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 0);
		  		    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, duty);
		  		    break;
			}
		}

		if(dhtCounter >= 10){
			dhtCounter = 0;
			sampleFlag = 1;
		}
	}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	static uint32_t lastPress = 0;

	//Change LED operation mode on button press
	if(GPIO_Pin == GPIO_PIN_13)
	{
		//Software debounce to avoid multiple triggers
		if(HAL_GetTick() - lastPress > 999){

		lastPress = HAL_GetTick();
		mode++;

		if(mode > BLUE)
			mode = WHITE;
		}
	}
}
